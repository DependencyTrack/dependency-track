package alpine.model;

import javax.jdo.annotations.Column;
import javax.jdo.annotations.PersistenceCapable;
import javax.jdo.annotations.Persistent;
import javax.jdo.annotations.PrimaryKey;
import java.util.Date;

@PersistenceCapable(table = "USER_SESSION")
public class UserSession {

    @PrimaryKey
    @Column(name = "TOKEN_HASH", length = 64, allowsNull = "false")
    private String tokenHash;

    @Persistent(defaultFetchGroup = "true")
    @Column(name = "USER_ID", allowsNull = "false")
    private User user;

    @Persistent
    @Column(name = "CREATED_AT", allowsNull = "false")
    private Date createdAt;

    @Persistent
    @Column(name = "EXPIRES_AT", allowsNull = "false")
    private Date expiresAt;

    @Persistent
    @Column(name = "LAST_USED_AT")
    private Date lastUsedAt;

    public String getTokenHash() {
        return tokenHash;
    }

    public void setTokenHash(String tokenHash) {
        this.tokenHash = tokenHash;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getExpiresAt() {
        return expiresAt;
    }

    public void setExpiresAt(Date expiresAt) {
        this.expiresAt = expiresAt;
    }

    public Date getLastUsedAt() {
        return lastUsedAt;
    }

    public void setLastUsedAt(Date lastUsedAt) {
        this.lastUsedAt = lastUsedAt;
    }

}
